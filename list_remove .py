#remove
fruit = ["apple", "banana", "cherry","melon","watermelon","papaya"]
fruit.remove("banana")
print(fruit)
fruit.remove("cherry")
print(fruit)
#pop
fruit.pop(2)
print(fruit)
fruit.pop()
print(fruit)
#delete
del fruit[0]
print(fruit)
#clear DELETES ITEMS IN THE LIST
fruit.clear()
print(fruit)