fruit = ["apple", "banana", "cherry","melon","watermelon","papaya"]
fruit[2] = "dragon fruit"
print(fruit)
fruit[5] = "pomegranate"
print(fruit)
#change a range of items 
fruit[1:4] = ["raspberries", "sapota","rambutan"]
print(fruit)
#change for negative index
fruit[-4:-1] = ["banana","cherry","melon"]
print(fruit)
fruit[-5:-4] = ["star fruit"]
print(fruit)

